package com.itk16.bk.pc.luyentoanlop1cungbe.model;

public class Item_anh {
    private int mAnh;
    private String mTenAnh;

    public Item_anh(int mAnh, String mTenAnh) {
        this.mAnh = mAnh;
        this.mTenAnh = mTenAnh;
    }

    public int getmAnh() {
        return mAnh;
    }

    public void setmAnh(int mAnh) {
        this.mAnh = mAnh;
    }

    public String getmTenAnh() {
        return mTenAnh;
    }

    public void setmTenAnh(String mTenAnh) {
        this.mTenAnh = mTenAnh;
    }
}
